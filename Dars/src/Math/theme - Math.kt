import java.lang.Math.pow
import java.lang.Math.nextDown
import java.lang.Math.nextUp
import kotlin.math.*

fun main(args: Array<String>) {

    println(E)                  //2.71
    println(PI)                 //3.14
    println(sign(4.0))       //+ & -
    println(abs(-5))         // |-5| = 5
    println(sin(45.0))
    println(cos(0.0))
    println(tan(90.0))
    println(ceil(4.06))       // 4.06 + -> 5
    println(floor(4.06))      // 4.06 - -> 4
    println(ln(7.0))
    println(log(8.0, 2.0))
    println(min(4, 5,))     // 4, 5 -> 4
    println(max(4, 5))     // 4, 5 -> 5
    println(8.0.nextDown())
    println(nextUp(8.0))
    println(8.0.nextTowards(7.0))
    println(pow(5.0, 2.0))       //darajaga ko'tarish
    println(round(5.5))       //yaxlitlash
    println(sqrt(25.0))       //ildiz


}
